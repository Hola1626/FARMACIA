    private void agregarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarVentaActionPerformed
        try {

            if (ventaActual == null) {
                throw new BusinessException("Primero debe buscar un cliente.");
            }

            if (medicamentoActual == null) {
                throw new BusinessException("Primero debe buscar un medicamento.");
            }

            int cantidad = (Integer) spCantidad.getValue();

            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this,
                        "Ingrese una cantidad válida.");
                return;
            }

            VentaService.agregarDetalle(ventaActual, medicamentoActual, cantidad);

            llenarTabla();

            actualizarTotal();

            limpiarMedicamento();

            spCantidad.setValue(1);

        } catch (BusinessException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage()
            );

        }
    }//GEN-LAST:event_agregarVentaActionPerformed

