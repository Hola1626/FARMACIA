    private void agregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarClienteActionPerformed

        String nombre = NombreCliente.getText().trim();
        String apellido = ApellidoCliente.getText().trim();
        String dni = DNICliente.getText().trim();
        String telefono = TelefonoCliente.getText().trim();

        if (nombre.isBlank()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre.");
            return;
        }

        if (apellido.isBlank()) {
            JOptionPane.showMessageDialog(this, "Ingrese el apellido.");
            return;
        }

        if (dni.length() != 8) {
            JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos.");
            return;
        }

        if (telefono.length() != 9) {
            JOptionPane.showMessageDialog(this, "El teléfono debe tener 9 dígitos.");
            return;
        }

        if (!telefono.startsWith("9")) {
            JOptionPane.showMessageDialog(this, "El teléfono debe comenzar con el número 9.");
            return;
        }

        try {

            ClienteService.registrarCliente(
                    dni,
                    nombre,
                    apellido,
                    telefono
            );

            JOptionPane.showMessageDialog(this,
                    "Cliente registrado correctamente.");

            cargarTablaCliente();
            limpiarFormularioClientes();

        } catch (BusinessException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );

        }
    }//GEN-LAST:event_agregarClienteActionPerformed

