    private void actualizarHistorial() {

        List<Turno> lista;

        String filtro = cboFiltroEstado.getSelectedItem().toString();

        if (filtro.equals("TODOS")) {

            lista = TurnoService.listarTurnos();

        } else {

            EstadoTurno estado = EstadoTurno.valueOf(filtro);

            lista = TurnoService.listarPorEstado(estado);

        }

        llenarTabla(lista);

    }

