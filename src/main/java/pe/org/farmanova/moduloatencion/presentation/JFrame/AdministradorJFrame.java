    private void crearTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearTurnoActionPerformed
        try {

            String dni = txtDni.getText().trim();

            if (dni.isEmpty()) {

                JOptionPane.showMessageDialog(this,
                        "Ingrese un DNI.");
                return;

            }

            if (dni.length() != 8) {

                JOptionPane.showMessageDialog(this,
                        "El DNI debe tener 8 dígitos.");
                return;

            }

            Cliente cliente = ClienteService.buscarCliente(dni);

            int dia = Integer.parseInt(cboDia.getSelectedItem().toString());
            int mes = Integer.parseInt(cboMes.getSelectedItem().toString());
            int anio = Integer.parseInt(cboAnio.getSelectedItem().toString());

            LocalDate fecha = LocalDate.of(anio, mes, dia);

            LocalTime hora = LocalTime.parse(cboHora.getSelectedItem().toString());

            PrioridadTurno prioridad = PrioridadTurno.valueOf(cboPrioridad.getSelectedItem().toString());

            TurnoService.registrarTurno(cliente, fecha, hora, prioridad);

            JOptionPane.showMessageDialog(this, "Turno registrado.");

            actualizarHistorial();
            actualizarCola();

            limpiarFormularioTurnos();

        } catch (DateTimeException e) {

            JOptionPane.showMessageDialog(this,
                    "La fecha seleccionada no es válida.");

        } catch (BusinessException ex) {
            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_crearTurnoActionPerformed

