    private void RegistrarMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarMedicamentoActionPerformed
        try {

            String nombre = txtNombreMedicamento.getText().trim();
            String laboratorio = txtLaboratorio.getText().trim();

            if (nombre.isBlank()) {
                throw new BusinessException("Ingrese el nombre del medicamento.");
            }

            if (laboratorio.isBlank()) {
                throw new BusinessException("Ingrese el laboratorio.");
            }

            double precio;

            try {
                precio = Double.parseDouble(txtPrecio.getText().trim());
            } catch (NumberFormatException e) {
                throw new BusinessException("Ingrese un precio válido.");
            }

            if (precio <= 0) {
                throw new BusinessException("El precio debe ser mayor que cero.");
            }

            int stock;

            try {
                stock = Integer.parseInt(txtStock.getText().trim());
            } catch (NumberFormatException e) {
                throw new BusinessException("Ingrese un stock válido.");
            }

            if (stock < 0) {
                throw new BusinessException("El stock no puede ser negativo.");
            }

            int dia = Integer.parseInt(cboDia.getSelectedItem().toString());
            int mes = Integer.parseInt(cboMes.getSelectedItem().toString());
            int anio = Integer.parseInt(cboAnio.getSelectedItem().toString());
            

            LocalDate fechaVencimiento = LocalDate.of(anio, mes, dia);

            MedicamentoService.registrarMedicamento(nombre, laboratorio, precio, stock,
                    fechaVencimiento);

            JOptionPane.showMessageDialog(
                    this,
                    "Medicamento registrado correctamente."
            );

            cargarTablaMedicamentos();

            limpiarFormularioMedicamentos();

        } catch (DateTimeException e) {

            JOptionPane.showMessageDialog(
                    this,
                    "La fecha de vencimiento no es válida."
            );

        } catch (BusinessException ex) {

            JOptionPane.showMessageDialog(
                    this,
                    ex.getMessage()
            );

        }
    }//GEN-LAST:event_RegistrarMedicamentoActionPerformed

